import { gsap } from "gsap";

export function initHero() {

    const tl = gsap.timeline({
        defaults: {
            ease: "power4.out"
        }
    });

    // ======================================
    // Header
    // ======================================

    tl.from(".header", {
        y: -60,
        opacity: 0,
        duration: 0.8
    });

    // ======================================
    // Верхний бейдж
    // ======================================

    tl.from(".hero-sub", {
        y: 30,
        opacity: 0,
        duration: 0.6
    }, "-=0.2");

    // ======================================
    // Каждая строка заголовка
    // ======================================

    tl.from(".hero .line", {

        yPercent: 120,

        opacity: 0,

        stagger: 0.18,

        duration: 1.15,

        ease: "power4.out"

    }, "-=0.2");

    // ======================================
    // Подзаголовок
    // ======================================

    tl.from(".hero p", {

        y: 30,

        opacity: 0,

        duration: 0.9

    }, "-=0.45");

    // ======================================
    // Кнопка
    // ======================================

    tl.from(".hero-button", {

        y: 30,

        opacity: 0,

        scale: 0.95,

        duration: 0.8

    }, "-=0.55");

    // ======================================
    // Медленное движение видео
    // ======================================

    gsap.to(".hero-video", {

        scale: 1.08,

        duration: 20,

        ease: "none"

    });

    // ======================================
    // Легкое плавание свечения
    // ======================================

    gsap.to(".hero::before", {
        duration: 8,
        repeat: -1,
        yoyo: true
    });

}
